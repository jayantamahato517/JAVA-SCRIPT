//marks system

let marks = 90;

if(marks >= 80){
    console.log("A+");
} else if(marks >= 60){
    console.log("A");
} else if(marks >= 33){
    console.log("B");
} else if(marks < 33){
    console.log("F");
}

// Traffic Light System
let color = "green";

if(color == "red"){
    console.log("Stop! light color is red");
} else if(color == "yellow"){
    console.log("Slow down. light color is yellow");
} else if(color == "green"){
    console.log("Go. light color is green");
}


